// ES5 Function Example

const addition = function(value1, value2){
	return value1 + value2;
}

console.log("Addition: " + addition(10,3));

// Ans Will be 13

// ES6 Arrow Function
// Arrow function will always anonymous

const multiplication = (value1, value2) => {
	return value1 * value2;
}

console.log("Multiplication: " + multiplication(2,10));	

// Ans Will be 20, this is example of arrow function

